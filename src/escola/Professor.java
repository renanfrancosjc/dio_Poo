public class Professor{
    private String nome;
    private int idade;
    private int materia;

    public String getNome(){
        return nome;
    }

    public void setNome(String newNome){
        nome = newNome;
    }

       public int getIdade(){
        return idade;
    }

    public void setIdade(int newIdade){
        idade = newIdade;
    }

    public String getMateria(){
        return materia;
    }

    public void setMateria(String newMateria){
        materia = newMateria;
    }
}